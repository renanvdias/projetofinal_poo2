     private void cadastrarLivro() {
        try {
            String id = idTextField.getText();
            String titulo = tituloTextField.getText();
            String autor = autorTextField.getText();

            Livro livro = new Livro(id, titulo, autor);

            // Adicione o livro ao BibliotecaManager
            BibliotecaManager.getInstance().adicionarLivro(livro);

            // Limpe os campos de texto após o cadastro
            idTextField.setText("");
            tituloTextField.setText("");
            autorTextField.setText("");

            // Exiba uma mensagem de sucesso (você pode usar JOptionPane)
            JOptionPane.showMessageDialog(this, "Livro cadastrado com sucesso.");

            // Atualize as telas de listagem automaticamente (graças ao padrão Observer)
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID do livro inválido. Insira um número válido.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
        
    private void botaoLivros1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoLivros1ActionPerformed
        cadastrarLivro();
    }//GEN-LAST:event_botaoLivros1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        LivroOnlineAdapter buscaLivro = new AdaptadorLivroOnline();
        Livro livro = buscaLivro.buscarLivroOnlinePorISBN(idTextField.getText());
    if (livro != null) {
         tituloTextField.setText(livro.getTitulo());
         autorTextField.setText(livro.getAutor());
    } else {
        // Trate a situação em que a busca falhou
        JOptionPane.showMessageDialog(null, "Falha ao buscar informações do livro.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

